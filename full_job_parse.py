def parse_from_single_co_page(input_url):
    """
    Given an URL of a page from Indeed with a full job description,
    parses interesting fields and uploads them to dataframe df,
    which is returned.
    """
    html = requests.get(input_url)
    page_soup = BeautifulSoup(html.content, 'html.parser', from_encoding="utf-8")
    df = pd.DataFrame(
        columns=['Title', 'Company', 'Location', 'City_and_State', 'Zip_Code', 'Job_Desc', 'Num_Reviews', \
                 'Review_Score', 'When_posted', 'Job_desc', 'URL_other_jobs'])
    #TODO Understand and eliminate appearance of dummy first column in csv
    try:
        title_ = page_soup.find('h3',
                                attrs={'class': 'icl-u-xs-mb--xs icl-u-xs-mt--none jobsearch-JobInfoHeader-title'}).text
    except:
        title_ = None
    try:
        company_ = page_soup.find('div', attrs={'class': "icl-u-lg-mr--sm icl-u-xs-mr--xs"}).text
    except:
        company_ = None
    try:
        location_ = str(page_soup.title.text).strip(str(title_)).strip(' -  ').strip(' - Indeed.com')
    except:
        location_ = None
    try:
        city_state = location_[:-6] # TODO Create a list of US States, write a function to
                                    #  Check for matching
                                    #  Split into two fields, City and State

    except:
        city_state = None
    try:
        zip_code_ = int(location_[-6:])  # TODO Write a function to find digits
    except:
        zip_code_ = None
    try:
        job_desc_ = page_soup.find('div', attrs={"class": "jobsearch-JobComponent-description"}).text
        # job_desc_ = page_soup.find("script", attrs={"body class": "miniRefresh"})
    except:
        job_desc_ = None
        # TODO Run against use cases and find some kind of formatting heirarchy
        #  Currently imported in bulk
    try:
        num_reviews_ = page_soup.find('div', attrs={"class": "icl-Ratings icl-Ratings--gold icl-Ratings--md"}).text[
                       :-55]
        # TODO convert to integer
    except:
        num_reviews_ = None
    try:
        review_score_ = page_soup.find('div', attrs={"class": "icl-Ratings icl-Ratings--gold icl-Ratings--sm"})
        review_score_ = str(review_score_.contents)
        review_score_ = (review_score_[16:19].strip())
        # TODO convert to integer
    except:
        review_score_ = None
    try:
        when_posted_ = page_soup.find('div', attrs={"class": "jobsearch-JobMetadataFooter"}).text
        when_posted_ = when_posted_[len(company_) + 3:when_posted_.find('- save')]
        # TODO Create 2 extra cols in dataframe, giving unit and value ie days/months unit
        #  Turn value into numeric
    except:
        when_posted_ = None
    # TODO less urgent extract URL from this list
    # try:
    #     url_other_jobs_ = page_soup.find('div', attrs={"class": "jobsearch-RelatedLinks-linkWrapper"})
    # except:
    #     url_other_jobs_ = None
    #
    # print('Title', title_)
    # print('Company', company_)
    # print('Location', location_)
    # print('City_and_State', city_state)
    # print('Zip_Code', zip_code_)
    # print('Num_Reviews', num_reviews_)
    # print('Review_Score', review_score_)
    # print('When_posted', when_posted_)
    # print('Job_desc', job_desc_)
    # # print('URL_other_jobs', url_other_jobs_)

    df = df.append(
        {'Title': title_, 'Company': company_, 'Location': location_, \
         'Job_Desc': job_desc_, 'Num_Reviews': num_reviews_, \
         'Review_Score': review_score_, 'When_posted': when_posted_, 'Job_desc': job_desc_}, ignore_index=True)
    return df


